public class T11ForWhileLoop {
    public static void main(String[] args) {

       //print numbers from 100 to 1 using for loop
        for (int i = 99; i >= 1 ; i-=2) {
            System.out.print(i+" ");
        }
        int i=99;
        while (i>=1){
            System.out.println(i);
            i-=2;
        }

        }
    }


