package class9;

public class E72DArrays {
    public static void main(String[] args) {


        int[][] numbers = {
                {10, 20, 30, 40},
                {15, 30, 35,45},
                {16,20, 32, 22}};

        for (int i = 0; i < numbers.length; i++) {
            for (int j = 0; j < numbers[i].length ; j++) {
                System.out.print(numbers[i][j]+" ");
            }
            System.out.println();
        }

        }
            }


